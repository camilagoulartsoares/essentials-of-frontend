/* EXERCÍCIO 1

Crie uma estrutura switch case para a variável dia, que imprima o nome do dia da semana (1 a 7).

*/

const dia = 3;

switch (dia) {
    case 1:
        console.log("SEGUNDA")
        break;
    case 2:
        console.log("TERÇA")
        break;
    case 3:
        console.log("QUARTA")
        break;
}


